import pytest


@pytest.mark.parametrize(
    "thai, expected",
    [
        # Придворная / королевская лексика (ราชาศัพท์)
        ("พระทันต์", "пˣраˇ-тˣан"),  # ฟัน
        ("พระเนตร", "пˣраˇ-не̄т`"),  # ตา
        ("พระฉายา", "пˣраˇ-ча̄´-йа̄"),  # เงา
        ("พระโอษฐ", "пˣраˇ-ɔ̄тˆ"),  # ปาก
        ("พระกลด", "пˣраˇ-клотˆ"),  # ร่ม
        ("พระมาลา", "пˣраˇ-ма̄-ла̄"),  # หมวก
        ("พระราชทาน", "пˣраˇ-ра̄т`-чаˇ-тˣа̄н"),  # ให้
        ("พระราชปุจฉา", "пˣраˇ-ра̄т`-чаˇ-путˆ-ча̄´"),  # ถาม
        ("ตรัส", "тратˆ"),  # พูด
    ],
)
def test_morev_royal_lexicon(thai: str, expected: str, morev_transcribe, nfd):
    out = morev_transcribe(thai)
    assert nfd(out) == nfd(expected)
