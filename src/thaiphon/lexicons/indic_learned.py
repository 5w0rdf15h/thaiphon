"""Bundled Indic learned readings.

Single source of truth for the curated Indic-origin lexicon — words whose
final short vowel is preserved or whose learned reading diverges from the
native pipeline default. The generator TSV is not read at runtime; this
module is the self-contained cache.

Regenerated by the build script; edit by hand only when applying targeted
corrections.
"""

# ruff: noqa: E501

