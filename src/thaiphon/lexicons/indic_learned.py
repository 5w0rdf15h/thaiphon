ReadingMode = Literal["learned_full", "variable", "thai_lexicalized", "clipped"]
ReadingProfile = Literal[
    "everyday", "careful_educated", "learned_full", "etalon_compat"
]


@dataclass(frozen=True, slots=True)
class IndicEntry:
    """One entry in the Indic learned lexicon.

    ``word`` always holds the everyday (default) reading. For ``mode``
    ``"variable"`` entries the fully-Indic "learned" alternative is
    exposed as a second :class:`PhonologicalWord` via ``alt_word`` when
    both are worth returning from the API. A separate
    ``etalon_compat_word`` slot carries the reading that historically
    matched the TLC reference, for callers opting into that profile;
    when unset, the everyday reading is used.
    """

    thai: str
    word: PhonologicalWord
    default_reading: str
    alt_reading: str | None
    mode: ReadingMode
    needs_linking_a: str
    source_confidence: str
    notes: str
    alt_word: PhonologicalWord | None = None
    etalon_compat_word: PhonologicalWord | None = None

    def resolve(self, profile: ReadingProfile) -> PhonologicalWord:
        """Return the :class:`PhonologicalWord` for the requested profile.

        For ``learned_full`` on a ``variable`` entry, the alternate
        learned reading is returned when one has been bundled; otherwise
        the everyday reading is used (so callers always get a real
        pronunciation back).

        For ``etalon_compat`` we return ``etalon_compat_word`` when it
        has been bundled for the entry, else fall back to the everyday
        reading.
        """
        if profile == "learned_full" and self.mode == "variable" and self.alt_word is not None:
            return self.alt_word
        if profile == "etalon_compat" and self.etalon_compat_word is not None:
            return self.etalon_compat_word
        return self.word


_T = Tone
_VL = VowelLength
_TM = ToneMark
_EC = EffectiveClass
_ST = SyllableType


def _P(symbol: str, *, asp: bool = False, son: bool = False) -> Phoneme:
    return Phoneme(symbol=symbol, is_aspirated=asp, is_sonorant=son)


def _cluster(a: Phoneme, b: Phoneme) -> Cluster:
    return Cluster(first=a, second=b)


def _syl(
    *,
    onset: Phoneme | Cluster | None,
    vowel: Phoneme,
    length: VowelLength,
    coda: Phoneme | None,
    tone: Tone,
    eclass: EffectiveClass,
    stype: SyllableType,
    raw: str = "",
) -> Syllable:
    return Syllable(
        onset=onset,
        vowel=vowel,
        vowel_length=length,
        coda=coda,
        tone=tone,
        tone_mark=ToneMark.NONE,
        effective_class=eclass,
        syllable_type=stype,
        raw=raw,
    )


def _word(thai: str, syllables: tuple[Syllable, ...]) -> PhonologicalWord:
    return PhonologicalWord(
        syllables=syllables,
        confidence=1.0,
        source="lexicon",
        raw=thai,
    )


